function baseForName(name) {
  if (BLOCK_BASE[name] !== undefined) return BLOCK_BASE[name];
  if (name.includes('leaves')) return 7;
  if (name.includes('grass') || name === 'fern' || name.includes('vine')) return 1;
  if (name.includes('sand')) return 2;
  if (name.includes('snow')) return 8;
  if (name.includes('ice')) return 5;
  if (name.includes('water') || name.includes('bubble')) return 12;
  if (name.includes('lava') || name.includes('fire')) return 4;
  if (name.includes('log') || name.includes('planks') || name.includes('wood') || name.includes('stem'))
    return 13;
  if (name.includes('wool') || name.includes('carpet') || name.includes('banner')) return 36;
  if (name.includes('terracotta') || name.includes('concrete')) return 15;
  if (name.includes('copper')) return 39;
  if (name.includes('amethyst') || name.includes('sculk')) return 31;
  if (name.includes('nether') || name.includes('crimson') || name.includes('warped')) return 35;
  if (name.includes('end_')) return 35;
  if (name.includes('prismarine')) return 23;
  if (name.includes('redstone')) return 29;
  if (name.includes('gold')) return 30;
  if (name.includes('diamond') || name.includes('emerald') || name.includes('lapis')) return 25;
  if (name.includes('iron')) return 6;
  if (name.includes('stone') || name.includes('deepslate') || name.includes('tuff') || name.includes('andesite') || name.includes('diorite') || name.includes('granite'))
    return 11;
  if (name.includes('dirt') || name.includes('mud') || name.includes('farmland') || name.includes('path'))
    return 10;
  if (name.includes('glass')) return 36;
  if (name.includes('bedrock') || name.includes('obsidian') || name.includes('barrier')) return 21;
  return 11;
}

function protocolForBlock(name) {
  const base = baseForName(name);
  if (base === 0) return 255;
  const pid = base * 4 + VARIANT;
  return pid < 248 ? pid : 255;
}

/** @param {import('minecraft-data').Block} block */
function propsFromStateIndex(block, stateId) {
  const states = block.states || [];
  if (!states.length) return {};
  let index = stateId - block.minStateId;
  const props = {};
  for (let i = states.length - 1; i >= 0; i--) {
    const s = states[i];
    const n = s.num_values;
    const valIndex = index % n;
    index = Math.floor(index / n);
    /* Java 1.21.10 wire global ids: snowy valIndex 0 = true, 1 = false (inverted vs
     * minecraft-data's prop labels). Verified on captures: grass with air above uses id 9
     * (md "true"), snow cover above uses id 8 (md "false"). */
    if (s.type === 'bool' && s.name === 'snowy')
      props[s.name] = valIndex ? 'false' : 'true';
    else if (s.type === 'bool') props[s.name] = valIndex ? 'true' : 'false';
    else props[s.name] = s.values[valIndex];
  }
  return props;
}

/** @param {import('minecraft-data').Block} block */
function formatStateName(block, stateId) {
  const props = propsFromStateIndex(block, stateId);
  const keys = Object.keys(props);
  if (!keys.length) return `minecraft:${block.name}`;
  return `minecraft:${block.name}[${keys.map((k) => `${k}=${props[k]}`).join(',')}]`;
}

function cStringLiteral(s) {
  return `"${String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
}

let maxState = 0;
for (const k of Object.keys(mcData.blocksByStateId)) {
  const id = Number(k);
  if (id > maxState) maxState = id;
}

const table = new Uint8Array(maxState + 1);
table.fill(255);

for (let sid = 0; sid <= maxState; sid++) {
  const block = mcData.blocksByStateId[sid];
  if (!block) continue;
  table[sid] = protocolForBlock(block.name);
}

